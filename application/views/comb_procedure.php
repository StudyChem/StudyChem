<h1>Procedure:</h1>
<h2>As performed in a real lab:</h2>
<ul>
    <li>Take  a small quantity of calcium oxide (quick lime) in a beaker.</li>
    <li>Slowly  add water to the quick lime taken in the beaker. Pour enough water so that the  quick lime remains submerged in the water.</li>
    <li>Touch  the beaker with fingers to note the temperature.</li>
    <li>Leave  the beaker undisturbed for some time.</li>
    <li>Take  a small quantity of clear solution from the beaker in a test tube and blow  exhaled air into the lime water.</li>
</ul>
<h2>Simulator Procedure:</h2>
<ul>
    <li>Move the mouse over each beaker  to see the name of  each reactant.</li>
    <li>Click on the beaker to pour  water into the beaker containing quick lime.</li>
    <li>You can see the increase of  temperature in the thermometer.</li>
    <li>Move the mouse over the beaker  to know the product obtained.</li>
    <li>You can also view the inference  by clicking on the inference icon.</li>
    <li>Click on the dropper to take  lime water from the beaker and pour it into the test tube.</li>
    <li>Click on the test tube to blow  exhaled air into it using a glass tube.</li>
    <li>You can see the inference by  clicking on the inference icon.</li>
    <li>Click on the ‘Reset’ to redo  the experiment.</li>
</ul>
<h1>Observations:</h1>
<ul>
    <li>When  water is added to quick lime, a suspension of slaked lime is formed.</li>
    <li>On  touching the beaker we feel it is hot.</li>
    <li>A  clear solution appears as the suspension of slaked lime settles down to the bottom of the beaker.</li>
    <li>On  blowing exhaled air that contains sufficient carbon dioxide, lime water turns  milky.</li>
</ul>
<h1>Inferences:</h1>
<ul>
    <li>Quick  lime reacts vigorously with water to produce slaked lime releasing a large  amount of heat. So the reaction is highly exothermic.</li>
</ul>
<p style="text-align: center;"><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<img alt="" src="<?php echo base_url();?>assests/img/combinationReactionEqn5.gif" height="34" width="310">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<ul>
    <li>Slaked  lime is only slightly soluble in water, so it forms a suspension of slaked lime  in water.</li>
    <li>The  clear solution obtained after the suspension settles is called  lime water.</li>
    <li>Exhaled  air contains CO2 which turns lime water milky.</li>
    <li>Calcium  oxide and water react to form only a single product,&nbsp; calcium hydroxide.So the  reaction is a combination reaction.</li>
</ul>
