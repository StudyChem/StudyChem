    <h1>Procedure:</h1>
    <h2>Real Lab Procedure:</h2>
    <ul>
        <li>Take about 2g of ferrous  sulphate crystals in a dry boiling tube and note the colour of the crystals.</li>
        <li>Hold the boiling tube with a  test tube holder and heat the boiling tube over the flame of a burner.</li>
        <li>Smell the gas being emitted.</li>
        <li>Observe the colour of the  crystals after heating.</li>
    </ul>
    <h2>To use the simulator:</h2>
    <ol>
        <li>Move  the mouse over each beaker to see the name of each reactant.</li>
        <li>First  click on the beaker to pour water into quick lime.</li>
        <li>You  can see the increase of temperature in the thermometer.</li>
        <li>Move  the mouse over the beaker to see the name of the product.</li>
        <li>You  can see the inference by clicking on the inference icon.</li>
        <li>Click  on the dropper to take lime water from the beaker and pour it into the test  tube.</li>
        <li>Click  on the test tube to blow exhaled air into it using a glass tube.</li>
        <li>You  can see the inference by clicking on the inference icon.</li>
        <li>Click  on the ‘Reset’ to redo the experiment.</li>
    </ol>
    <h1>Observations:</h1>
    <ul>
        <li>The ferrous sulphate crystals  are light green in colour.</li>
        <li>The gas emitted has the  characteristic odour of burning sulphur.</li>
        <li>On heating, the colour changes  from light green to white.</li>
        <li>On further heating, the white  substance changes to dark brown solid.</li>
    </ul>
    <h1>Inference:</h1>
    <ul>
        <li>Ferrous sulphate crystals  contain water molecules (FeSO<sub>4</sub>. 7H<sub>2</sub>O). On heating,  ferrous sulphate crystals lose water and anhydrous ferrous sulphate (FeSO<sub>4</sub>)  is formed. So their colour changes from light green to white.</li>
        <p style="text-align: center;"><img alt="" src="<?php echo base_url();?>assests/img/DecompositionReactionProcEqn1.gif" height="80" width="370" align="middle"></p>
        <li>On further heating, anhydrous ferrous sulphate decomposes to form  ferric oxide (Fe<sub>2</sub>O<sub>3</sub>), sulphur dioxide (SO<sub>2</sub>)  and sulphur trioxide (SO<sub>3</sub>). So, the gas emitted smells like  burning sulphur.&nbsp;</li>
    </ul>
    <ul>
        <li>In this reaction, the single  reactant FeSO<sub>4 </sub>decomposes to form three different products. So, the  reaction is a decomposition reaction.</li>
    </ul>
