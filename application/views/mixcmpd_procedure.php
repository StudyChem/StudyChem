    <h2>Result:&nbsp;</h2>
    <ol>
        <li>When iron filings and sulphur powder are mixed, both retain their properties. It means they have not undergone any chemical reaction. Therefore, mixing of iron and sulphur is a physical change.</li>
        <li>When iron filings and sulphur powder are mixed and heated they undergo a chemical reaction&nbsp; and form ferrous sulphide (FeS). It is a new substance which has properties entirely different from Fe and S.Therefore, heating of mixture of iron and sulphur powder is a chemical change.</li>
    </ol>
    <h2>Precautions:</h2>
    <ol >
        <li>1. Care should be taken while heating iron and sulphur mixture.</li>
        <li>2. Do not brong carbon disulphide near the burner (carbon disulphide is volatile and its vapours catch fire).</li>
        <li>3. Do not inhale gases evolved directly, for example, hydrogen sulphide.&nbsp;</li>
