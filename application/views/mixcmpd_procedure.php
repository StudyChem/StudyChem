    <h2>Real Lab Procedure:&nbsp;</h2>
    <h3>Preparation of mixture using iron filings and  sulphur powder:</h3>
    <p>Take about 14g of fine iron filings and 8g of sulphur powder in a big petridish or a beaker. Mix them well to prepare a mixture of iron filings and sulphur powder. Label the mixture A.</p>
    <h3>Preparation of compound using iron filings and  sulphur powder:</h3>
    <p>Take  about 14g of fine iron fillings and 8g sulphur powder in a china dish. Mix them  well and heat the contents of the china dish, first gently, then strongly. Stir  it constantly till a black mass, i.e.,<strong> compound </strong>of sulphur and iron is  formed. When a black mass is formed, cool it and powder the black mass. Label  the compound powder as B.</p>
    <h3>To distinguish between the iron filings and sulphur  powder mixture and compound on the basis of:</h3>
    <ul>
        <li><strong>Appearance: Homogeneity and Heterogeneity:</strong> Take  some powder A (iron filings and sulphur powder mixture) in one watch glass and  mixture B black mass (compound) powder in another watch glass. Observe both of  them carefully. Record your observations in a table.<br>
        &nbsp;</li>
        <li><strong>Behaviour towards magnet:</strong> Place a  small quantity of mixture: A in a watch glass and move a magnet over it.<br>
        Place a small quantity of B (black powder) in  another watch glass and move the same magnet over it. Examine the movement of  the magnet and record your observations.</li>
        <li><strong>Action of Carbon Disulphide:</strong> Take a  small quantity of A in a watch glass and add 1ml of carbon disulphide (CS<sub>2</sub>)  to it. Observe the mixture for some time and then record your observations.<br>
        Take a small quantity of compound B in another watch glass and add 1ml of CS<sub>2</sub>  to it. Observe the compound for some time and record your observations. &nbsp;</li>
        <li><strong>Effect of Heat:</strong> Take a  small quantity of mixture A in a test tube and heat it. Observe the change in  the mixture after some time.<br>
        Take a small quantity of compound powder B in a test tube and heat it for the same  period as powder A was heated. Observe the change</li>
        <li><strong>Effect of heating with dil. HCl: </strong>Take a  small quantity of mixture A in a test tube and add few drops of dil. HCl into  it. Then heat the contents of the test tube gently and collect the gas formed  in another test tube. Then bring a glowing match stick near the mouth of the  test tube in which gas is collected and record your observations. <br>
        Now take a small quantity of compound B in a test  tube and add few drops of dil. HCl into it. Then heat the contents of the test  tube gently and pass the gas evolved through copper sulphate solution taken in  another test tube and record your observations.</li>
    </ul>
