<h1>Reaction of Iron Nails with Copper Sulphate Solution in Water</h1>
<h2>Materials Required:</h2>
<ul><img src="<?php echo base_url();?>assests/img/iron.png" alt="iron" height="425" width="900"></ul>
    <h2>Lab Procedure:</h2>
    <ul>
        <li>Clean two iron nails of sufficient size by  rubbing with sand paper so that their colour appears greyish.</li>
        <li>Take sufficient quantity of copper sulphate  solution in two test tubes and fix one test tube in each stand.</li>
        <li>Tie one iron nail with a thread and hang it  in one test tube so that it is completely immersed in copper sulphate solution.  Tie the other end of the thread with the stand.</li>
        <li>Keep the other nail in a petri dish for  comparison after the experiment.</li>
        <li>Keep the two test tubes undisturbed for  about 15 min.</li>
        <li>After 15 min. remove the iron nail immersed  in copper sulphate solution and put it in the petri dish.</li>
    </ul>
    <h2>Simulator Procedure:</h2>
    <p>You can select  the reactions from the drop down list of the ‘Select Reaction’.</p>
    <ol>
        <li>Click on the iron nail tied  with a thread to immerse it in one of the test tubes containing copper sulphate  solution.</li>
        <li>Once its done, wait till the  timer shows 15 minutes.</li>
        <li>You can see the inference by  clicking on the inference icon.</li>
        <li>&nbsp;Click on the thread to remove iron nail from  the test tube.</li>
        <li>You can view the zoom part of  the test tubes or iron nails by clicking on them.</li>
        <li>You can click the ‘Reset’  button to redo the experiment.</li>
    </ol>
    <h2>Observations:</h2>
    <ul>
        <li>There is a brown coating on the iron nail which  was dipped in the copper sulphate solution, whereas the iron nail placed in petri dish  shows greyish colour of iron.</li>
        <li>The colour of the copper sulphate solution  in which the iron nail was dipped turns light greenish, whereas the solution of copper  sulphate in the other test tube does not change.</li>
    </ul>
    <h2>Inferences:</h2>
    <ul>
        <li>The brown coating on the iron nail shows  that copper is deposited on the iron nail by displacing iron.</li>
        <li>The greenish colour of the solution in the  test tube shows that Fe<sup>2+</sup> ions are present in the solution.</li>
        <li>This shows that iron is more reactive than  copper as Fe<sup>2+</sup> ions have displaced Cu<sup>2+</sup> ions from copper  sulphate solution and form light greenish coloured ferrous sulphate solution.</li>
    </ul>
    <p style="text-align: center;"><img src = "<?php echo base_url();?>assests/img/cheReactionEq5.gif" alt="cheRectionEqn5" align="middle" height="27" width="351"><br>
    &nbsp;</p>
    <ul>
        <li>This is a single displacement reaction in  which copper has been displaced by iron from copper sulphate solution and a new  compound, ferrous sulphate, is formed. So, this reaction is a chemical change.&nbsp;</li>
    </ul>
    <h2>Precautions:</h2>
    <ol>
        <li>Clean the iron nails by rubbing them with sand paper to remove rust, dust or greasy surface.</li>
        <li>Keep the control experiment to compare the colour of iron nails and copper sulphate solution.</li>
        <li>Avoid touching copper sulphate solution or a nail dipped in copper sulphate solution. As copper sulphate is poisonous.<br>
        &nbsp;</li>
    </ol>
    <h1>Heating  of Copper Sulphate.</h1>
    <h2>Materials Required:</h2>
    <ul><img alt="" src = "<?php echo base_url();?>assests/img/Heating%20of%20copper%20sulphate%20materials.png" height="210" width="772"></ul>
        <h2>Lab Procedure:</h2>
        <ul>
            <li>Take a small amount of copper sulphate  crystals in a dry boiling tube.</li>
            <li>Hold the boiling tube with a test tube  holder.</li>
            <li>Heat the boiling tube over the flame of a burner  first gently and then strongly.</li>
            <li>Observe the colour of copper sulphate  crystals after heating for some time.</li>
            <li>Note the  water droplets along the sides of the boiling tube.</li>
            <li>Remove the boiling tube from the flame and  cool.</li>
            <li>Add 2-3 drops of water on the sample of  copper sulphate obtained after heating.</li>
        </ul>
        <h2>Simulator Procedure:</h2>
        <p>You can select  the reactions from the drop down list of the ‘Select Reaction’.</p>
        <ol>
            <li>Click on the holder to heat the  boiling tube containing hydrated copper sulphate crystals.</li>
            <li>Turn on the burner by clicking  on the knob of the burner.</li>
            <li>The burner turns off  automatically when blue coloured copper Sulpahte crystals changes its colour to  white.</li>
            <li>You can see the inference by  clicking on the inference icon.</li>
            <li>Click on the dropper to add  water into the boiling tube containing anhydrous copper sulphate.</li>
            <li>You can see the inference by  clicking on the inference icon.</li>
            <li>You can click the ‘Reset’  button to redo the experiment.&nbsp;</li>
        </ol>
        <h2>Observations:</h2>
        <ul>
            <li>On heating, the colour of copper sulphate  crystals changes from blue to white.</li>
            <li>There are water droplets on the walls of  the boiling tube.</li>
            <li>On adding 2-3 drops of water white copper  sulphate turns blue.</li>
        </ul>
        <h2>Inferences:</h2>
        <ul>
            <li>Copper sulphate crystals contain water of  crystallisation (CuSO<sub>4</sub>.5H<sub>2</sub>O).</li>
            <li>On heating, hydrated copper sulphate loses  its water molecule and forms white coloured anhydrous copper sulphate.&nbsp;</li>
            <p style="text-align: center;"><img src = "<?php echo base_url();?>assests/img/cheReactionEq6.gif" alt="" align="middle" height="31" width="347"></p>
        </ul>
        <ul>
            <li>On adding water anhydrous copper sulphate  changes to hydrated copper sulphate.</li>
            <li>It is a reversible chemical change.</li>
        </ul>
        <h2>Precautions:</h2>
        <ol>
            <li>Keep the mouth of the test tube away from your face and also from other classmates.</li>
            <li>Always use test tube holder while heating the test tube.</li>
            <li>Take a small quantity of copper sulphate crystals.</li>
        </ol>
        <h1>Reaction  of Zinc with Dilute Sulphuric Acid</h1>
        <h2>Materials Required:</h2>
        <ul><img style="width: 840px; height: 350px;" alt="" src = "<?php echo base_url();?>assests/img/Reaction%20of%20zinc%20materials.png"></ul>
            <h2>Lab Procedure:</h2>
            <ul>
                <li>Take a small quantity of dil. H<sub>2</sub>SO<sub>4</sub>  in a conical flask.</li>
                <li>Add a small quantity of zinc granules&nbsp; to  it.</li>
                <li>Bring a wet blue and red litmus paper near  the mouth of the conical flask one by one.</li>
                <li>Cork the flask with a one bored cork and  insert a jet tube through the hole in the cork.</li>
                <li>Bring a burning candle near the mouth of  the fine jet tube.</li>
                <li>Pour a small amount of the solution from  the conical flask into a test tube.</li>
                <li>Add few drops of sodium hydroxide solution  into it. A white gelatinous precipitate is formed.</li>
                <li>Add excess of NaOH into the gelatinous  precipitate.</li>
                <li>The precipitate is soluble in excess of  NaOH.</li>
                <li>Take a small amount of the solution in  another test tube and pass H<sub>2</sub>S gas through it using Kipp&#039;s apparatus.</li>
                <li>A white precipitate is formed.</li>
            </ul>
            <h2>Simulator Procedure:</h2>
            <p>You can select  the reactions from the drop down list of the ‘Select Reaction’.</p>
            <ul type="disc">
                <li>You can select the tests from  drop down list of ‘Select identification test’.
                <ul type="1">
                    <li><strong>Litmus test</strong>
                    <ul type="disc">
                        <li>Zinc granules are added into  the test tube containing dil. H<sub>2</sub>SO<sub>4 </sub>using a spatula.</li>
                        <li>Click &amp; drag the litmus  papers one by one over the mouth of the test tube to show it on the gas coming  out of the test tube.</li>
                        <li>You can see the inference by  clicking on the inference icon.</li>
                        <li>You can click the ‘Reset’  button to redo the test.</li>
                    </ul>
                    </li>
                    <li><strong>Hydrogen pop test</strong>
                    <ul type="disc">
                        <li>Click on the spatula to add  zinc granules into the test tube containing dil. H<sub>2</sub>SO<sub>4</sub>.</li>
                        <li>The test tube is corked by a  single bored cork and a fine jet tube is inserted through the cork into the  test tube.</li>
                        <li>Click &amp; drag the burning  candle towards the mouth of the fine jet tube.</li>
                        <li>You can see the inference by  clicking on the inference icon.</li>
                        <li>You can click the ‘Reset’  button to redo the test.</li>
                    </ul>
                    </li>
                    <li><strong>ZnSO<sub>4</sub> with NaOH</strong>
                    <ul type="disc">
                        <li>Zinc granules are added into  the test tube containing dil. H<sub>2</sub>SO<sub>4</sub> using a spatula.</li>
                        <li>You can see the inference by  clicking on the inference icon.</li>
                        <li>Click on the dropper to add  NaOH into the test tube containing zinc sulphate solution.</li>
                        <li>You can see the inference by  clicking on the inference icon.</li>
                        <li>Again click on the dropper to  add excess of NaOH into the test tube.</li>
                        <li>You can see the inference by  clicking on the inference icon.</li>
                        <li>You can click the ‘Reset’  button to redo the test.</li>
                    </ul>
                    </li>
                    <li><strong>ZnSO<sub>4</sub> with H<sub>2</sub>S</strong>
                    <ul type="disc">
                        <li>Zinc granules are added into  the test tube containing dil. H<sub>2</sub>SO<sub>4</sub> using a spatula.</li>
                        <li>Click on the knob of Kipps  apparatus to pass H<sub>2</sub>S gas through the solution in the test tube.</li>
                        <li>You can see the inference by clicking  on the inference icon.</li>
                        <li>You can click the ‘Reset’  button to redo the test.</li>
                    </ul>
                    </li>
                </ul>
                </li>
            </ul>
            <h2>Observations:</h2>
            <ul>
                <li>After adding zinc granules to dil. H<sub>2</sub>SO<sub>4</sub>  evolution of gas bubbles occurs.</li>
                <li>The gas coming out does not change the  colour of blue and red litmus papers.</li>
                <li>On bringing the flame, the gas  coming out of the jet tube burns instantaneously with a small explosion and  produce a pop sound and the flame dips out.</li>
                <li>On adding NaOH to the small amount of  solution taken from the conical flask, white gelatinous precipitate is formed  which is soluble in excess of NaOH.</li>
                <li>On passing hydrogen sulphide gas through the  solution taken in the test tube a white precipitate is formed.</li>
            </ul>
            <h2>Inferences:</h2>
            <ul>
                <li>Zinc reacts with dil.H<sub>2</sub>SO<sub>4</sub>  to form zinc sulphate and evolve hydrogen gas.</li>
            </ul>
            <p style="text-align: center;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <img src = "<?php echo base_url();?>assests/img/cheReactionEq8.gif" alt="" align="middle" height="26" width="330">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
            <ul>
                <li>The test with red and blue litmus papers  shows that hydrogen gas is neither acidic nor basic in nature.</li>
            </ul>
            <ul>
                <li>NaOH reacts with ZnSO<sub>4</sub>  to form a white gelatinous precipitate of zinc hydroxide (Zn(OH)<sub>2</sub>)  which is soluble in excess of NaOH.</li>
            </ul>
            <p style="text-align: center;"><img src = "<?php echo base_url();?>assests/img/cheReactionProcEqn1.gif" alt="" align="middle" height="28" width="477"><br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
            <ul>
                <li>ZnSO<sub>4</sub> reacts with H<sub>2</sub>S to form a white  precipitate of zinc sulphide (ZnS).</li>
            </ul>
            <p style="text-align: center;"><img src = "<?php echo base_url();?>assests/img/cheReactionProcEqn2.gif" alt="" align="middle" height="26" width="352"></p>
            <ul>
                <li>Zinc metal is more reactive  than hydrogen.</li>
            </ul>
            <ul>
                <li>This is a single displacement  reaction of a non-metal by a metal.&nbsp;</li>
            </ul>
            <ul>
                <li>ZnSO<sub>4</sub> is entirely  different in chemical composition and chemical properties than Zn and H<sub>2</sub>SO<sub>4</sub>. So it is a chemical  change</li>
            </ul>
            <h2>Precautions:</h2>
            <ol>
                <li>Use the chemicals judiciously.</li>
                <li>Do not inhale gases evolved directly.</li>
                <li>Hydrogen gas instantaneously burns with mild explosion. Therefore, a fine jet tube should be used to see the burning of hydrogen.</li>
                <li>Handle acids and alkalies carefully.</li>
            </ol>
            <h1>Reaction between Sodium Sulphate and Barium Chloride.</h1>
            <h2>Materials Required:</h2>
            <p><img alt="" src = "<?php echo base_url();?>assests/img/Reaction%20between%20sodium%20sulphate%20&amp;%20barium%20materials.png" height="375" width="900"></p>
            <h2>Lab Procedure:</h2>
            <ul>
                <li>Take a small amount of barium  chloride solution in a test tube and pour it into a conical flask.</li>
                <li>Then take a small amount of  sodium sulphate solution in another test tube and add it into barium chloride  solution taken in the conical flask.</li>
                <li>Stir the contents of the conical flask and keep it undisturbed for some time.</li>
                <li>After some time decant  the upper solution from the conical flask and add a small amount of dil. HCl to  the precipitate in the conical flask.</li>
            </ul>
            <h2>Simulator Procedure:</h2>
            <p>You can select  the reactions from the drop down list of the ‘Select Reaction’.</p>
            <ol>
                <li>Click on the bottle containing  BaCl<sub>2</sub> to pour it into the conical flask.</li>
                <li>Click on the bottle containing  Na<sub>2</sub>SO<sub>4</sub> to pour it into the conical flask containing BaCl<sub>2</sub>  .</li>
                <li>Stir the contents of the  conical flask using a glass rod by clicking on the conical flask.</li>
                <li>You can see the inference by  clicking on the inference icon.</li>
                <li>Now click on the conical flask  to decant the upper solution into a beaker.</li>
                <li>Click on the bottle containing  dil. HCl to pour it into the same conical flask.</li>
                <li>Click on the conical flask to  shake it well.</li>
                <li>You can see the inference by  clicking on the inference icon.</li>
                <li>You can click the ‘Reset’  button to redo the experiment.</li>
            </ol>
            <h2>Observation:</h2>
            <ul>
                <li>On adding sodium sulphate to  barium chloride a white precipitate is immediately formed which is insoluble in  dil.HCl.</li>
            </ul>
            <h2>Inferences:</h2>
            <ul>
                <li>Sodium sulphate chemically reacts with barium chloride in the form of their aqueous  solution to form a white precipitate of barium sulphate.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</li>
                <p style="text-align: center;"><img src = "<?php echo base_url();?>assests/img/cheReactionEq9.gif" alt="" align="middle" height="30" width="443"></p>
            </ul>
            <ul>
                <li>This reaction is a chemical change, as the products are entirely  different from the reactants in chemical composition and chemical properties.&nbsp;</li>
            </ul>
            <h2>Precautions:</h2>
            <ol>
                <li>Use the chemicals judiciously.</li>
                <li>Keep the mouth of the test tube away from your face and also from other classmates.</li>
                <li>Handle the acids and alkali carefully.</li>
