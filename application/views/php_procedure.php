<h1>The Procedure:&nbsp;</h1>
<h2>As performed in a real lab:</h2>
<ul type="1">
    <li>Take six strips pH paper and place them on a glazed  tile. Mark them 1 to 6.</li>
    <li>Take the test solutions in separate test tubes.  Dissolve the solid substance by adding distilled water to it. Label the test  tubes.</li>
    <li>Now, place a drop of the test solution on one strip  of the pH paper with the help of a fine dropper or glass rod. Use a fresh  dropper&nbsp;for each test solution.</li>
    <li>Observe the colour produced and match it with the  different colour shades of the standard colour pH chart.</li>
    <li>Note down the &nbsp;colour of the&nbsp;pH&nbsp;from the colour  chart&nbsp;that matches most closely with the colour producd on the pH paper.</li>
    <li>Similarly, find the pH value of the remaining  samples by using a fresh strip of pH paper and a separate glass rod or fine dropper for  each one.&nbsp;</li>
</ul>
<h2>As performed using the  simulator:</h2>
<ul type="1">
    <li>You can select the aqueous solution type you  want to find the pH value of, from the ‘Select Aqueous Solution’ drop down list  (vegetable &amp; fruit juices, household items, acids and bases in the lab or salts in water).</li>
    <li>Now choose any one of the solutions in the  beaker by clicking on it.</li>
    <li>There are two ways of finding the pH value of  the solution:</li>
    <p><strong>Using the pH strip:</strong></p>
    <ul type="a">
        <li>Click and drag the dropper from the stand and  move into the solution in the beaker to collect the solution in the dropper.</li>
        <li>Still holding the dropper, move it from the  beaker over to the pH strip and release it.&nbsp;</li>
        <li>To find the pH value of the solution, select  the colour from the menu on the left by clicking and dragging it to the pH  strip and comparing it.</li>
        <li>The colour that matches with the spot on the  pH strip indicates the pH value of the solution.</li>
    </ul>
    <p><strong>Using the Universal Indicator:</strong></p>
    <ul type="a">
        <li>Click and drag the dropper from the universal  indicator bottle and move it into the solution in the beaker to drop the universal  indicator into it.</li>
        <li>To find the pH value of the solution, select  the colour from the menu on the left by clicking and dragging it next to the  solution in the beaker and comparing it.</li>
        <li>The colour that matches with the solution in  the beaker indicates the pH value of the solution.</li>
    </ul>
</ul>
<p><strong>Note:</strong></p>
<ul>
    <li>Once  test is done using the Universal Indicator, you cannot do it with the pH strip. To do it with the pH strip, click the ‘Reset’  button and vice versa.</li>
    <li>The  ‘Reset’ button can be used to redo the experiment with other solutions.</li>
</ul>
