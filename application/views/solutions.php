<h1>The Theory</h1>
<h2>What is a solution?</h2>
<p><img alt="" style="width: 428px; height: 200px;" src="<?php echo base_url();?>assests/img/sugar1.png" align="right"></p>
<p>A<strong> Solution</strong> is a homogeneous mixture of two or more substances. The component that is present in a larger amount is called the solvent. The component present in a smaller quantity is called the solute.&nbsp;</p>
<p>The maximum mass of substance that is dissolved in 100 g of a solvent under a particular condition of temperature and pressure (for example, the conditions of room temperature and pressure) is referred to as the <strong>solubility </strong>of that substance in that solvent under the specified conditions. This solution so obtained is said to be a <em>saturated solution</em>.</p>
<h2>What is meant by a saturated solution?</h2>
<p>It means that no more solute is dissolved in the solvent at the given temperature and pressure. Solubility varies with temperature and pressure. For example, solubility of salt/sugar increases with the rise in temperature. The solution in which no more solute is soluble in the solvent, at given temperature and pressure conditions is said to be a saturated solution under those specified conditions.&nbsp;</p>
<p>The solutions which we generally come across are not saturated. They contain a smaller amount of solute. The compositions of such solutions are expressed as <strong>concentration, </strong>which refers to the quantity of a solute dissolved in a definite weight or volume of the solution. The concentration may be expressed in terms of either mass or moles. The number of moles of a solute present in one litre of a solution is called the molarity of that solution. If 58.5 g NaCl (Na = 23 + Cl = 35. 5) is present in 1000 ml of a solution, it will be known as 1M (1 molar) solution of NaCl.&nbsp;</p>
<p>Dispersion may give rise to a <strong>homogeneous </strong>(solution) or <strong>heterogeneous </strong>material (a suspension or a colloid). The distinction lies in the size of the dispersed particles.&nbsp;<img src="<?php echo base_url();?>assests/img/cls9_distin_bet_sol110.jpg" alt="" align="right" height="230" width="375"></p>
<p>In suspension, the particle size is of the order of 10<sup>-5</sup> cm or larger. The particles can be seen distinctly in the dispersion medium (solvent) by the naked eye or under an ordinary&nbsp;microscope. The suspensions are not very stable. The suspended particles may settle down after sometime, e.g.,muddy water or smoke in the air.&nbsp;</p>
<p>Colloids have&nbsp; particles smaller than 10<sup>-5</sup> cm (generally 10<sup>-7</sup> to 10<sup>-5</sup> cm). The dispersed particles show very little tendency to separate. Colloid particles cannot be seen by the naked eye or under an ordinary microscope. Colloids are stable. Gum, milk and blood are typical colloids.&nbsp;</p>
<p>In a solution, the particles are of molecular dimensions, smaller than 10<sup>-7</sup> cm. Solutions are transparent and homogeneous.&nbsp;</p>
