<h1>The Theory</h1>
<h2>Let’s study how matter is classified!</h2>
<p>All matter can be classified into two categories: pure substances and mixtures. A pure substance consists of elements or compounds. Furthermore, mixtures can be classified into homogeneous and heterogeneous.</p>
<h1 style="text-align: center;">&nbsp;<img src="<?php echo base_url();?>assests/img/mixtureAndCompoundPic.jpeg" alt="mixtureAndCompoundPic" height="302" width="423">&nbsp;&nbsp;&nbsp;</h1>
<h2>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
What are elements and compounds?</h2>
<h3>Elements:</h3>
<p>&nbsp;Elements are the simplest substances in nature. There are around 90 naturally occurring elements on earth. They cannot be broken down into anything simpler by physical or chemical means. They consist of only one kind of atom and they can exist as either atoms (eg. argon, calcium, aluminium) or molecules (oxygen, nitrogen).</p>
<p style="text-align: center; ">&nbsp;<img src="<?php echo base_url();?>assests/img/cls9_mix_comp_110.jpg" alt="cls9_mix_comp_110" height="126" width="336"></p>
<h3>Compounds:</h3>
<p>A compound is formed when two or more elements chemically combine together. The elements react chemically and form chemical bonds between the atoms of the elements. In compounds the constituent elements are always present in fixed proportions and therefore a compound has a definite chemical composition.</p>
<p>The components of a compound lose their individual properties and the properties of a compound are different from their individual components. For example, Iron is an element which is metallic in nature and has magnetic properties and oxygen is an element which is a colorless odorless gas. When iron rusts, it reacts with atmospheric oxygen to form the compound iron oxide.&nbsp;</p>
<p>Iron oxide has different properties from those of iron and sulphur. Iron oxide is a reddish brown colored solid and is non-magnetic in nature. The components of a compound may be separated only by chemical means. Compounds are homogeneous in nature and have fixed melting points and boiling points.</p>
<p>Examples: water (H<sub>2</sub>O), sodium chloride (NaCl), ammonia (NH<sub>3</sub>), ethanol (C<sub>2</sub>H<sub>5</sub>OH)</p>
<p style="text-align: center; "><br>
&nbsp;<img src="<?php echo base_url();?>assests/img/cls9_mix_comp_22.jpg" alt="cls9_mix_comp_22" height="128" width="350"></p>
<h2>Now, what are mixtures and how they are classified?</h2>
<h3>Mixtures:</h3>
<p>A mixture is a combination of two or more substances which are not chemically the same. The composition of a mixture is variable.&nbsp; In a mixture, the components are present in any proportion and they can be identified visually within the mixture. In a mixture, the individual components retain their own identities.</p>
<p>For example, when carbon disulphide is added to a mixture of iron and sulphur, sulphur first dissolves and then reappears in its solid state. First add a small amount of CS<sub>2 </sub>into the mixture of iron filings and sulphur taken in a test tube and shake gently. After shaking the iron filings settle down to the bottom of the beaker and sulphur dissolves in CS<sub>2</sub> to give a clear yellow solution.&nbsp;&nbsp; Then, pour this clear yellow solution into a china dish and after some time the yellow coloured sulphur powder reappears in the china dish as CS<sub>2</sub> evaporates.</p>
<p>The properties of a mixture are the same as the properties of individual compounds. Individual components of a mixture may be separated by physical methods. For example, a magnet can be used to separate iron from a mixture of iron and sulphur.</p>
<p>Mixtures can be homogeneous mixtures or heterogeneous mixtures.</p>
<p><strong>Homogeneous mixtures:</strong> Homogeneous mixtures have uniform composition and appearance throughout. A homogeneous mixture is also called a true solution.</p>
<p>Example: sugar solution, ocean water, soft drinks, etc.</p>
<p><strong>Heterogeneous mixtures:</strong> Heterogeneous mixtures consist of different substances.</p>
<p>Examples: mixture of iron and sulphur, mixture of sand and table salt, etc.</p>
