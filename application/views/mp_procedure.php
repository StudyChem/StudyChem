<h1>The Procedure:</h1>
<h2>Real Lab Procedure:</h2>
<ul>
    <li>Take some ice cubes and dry them using a filter  paper and quickly put them in a beaker.</li>
    <li>Place the beaker over a wire gauze kept over a  tripod stand.</li>
    <li>Suspend a thermometer (temperature range -10 to  110°C) with the help of a clamp stand so that its bulb remains in the middle of  the ice cubes.</li>
    <li>Ice cubes are moderately heated by a gas burner and  stirred continuously using a glass rod.</li>
    <li>Note the temperature (t1) when the ice starts  melting.</li>
    <li>Continue heating the ice.</li>
    <li>Note the temperature (t2) when the ice has melted completely.</li>
    <li>Record your observations in tabular form.</li>
</ul>
<h2>Simulator Procedure:</h2>
<ul>
    <li>First put the ice cubes in the beaker by clicking  on the ‘Add Ice Cubes’ button.</li>
    <li>This done, turn on the burner by clicking the ‘Turn on Burner’ button to heat the beaker.</li>
    <li>The ‘Stir’ button can be used to stir the  contents in the beaker.</li>
    <li>The burner turns off automatically when the  ice melts and the temperature will be shown.</li>
    <li>You can click the ‘Reset’ button to redo the  experiment.</li>
</ul>
