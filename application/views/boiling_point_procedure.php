<h1>The Procedure:</h1>
<h2>Real Lab Procedure:</h2>
<ul type="disc">
    <li>Take       about 25ml of distilled water in a boiling tube and add 2-3 small pieces       of pumice stone.</li>
    <li>Close       the mouth of the boiling tube with a rubber cork that has two bores and       clamp it with the stand.</li>
    <li>Introduce       a thermometer (temperature range -10 to 110°C) in one bore of the cork of       the boiling tube. Keep the bulb of the thermometer&nbsp; about 3-5 cm above       the surface of the water.</li>
    <li>Then       introduce one end of a delivery tube in the second bore of the cork.</li>
    <li>Place       a 250ml beaker below the second end of the delivery tube to collect the       condensed water.</li>
    <li>Heat       the boiling tube gently, preferably by rotating the flame.</li>
    <li>Note       the temperature (t1) when the water starts boiling.</li>
    <li>Continue       to heat the water till the temperature becomes constant, and the water       remains boiling. Note the constant temperature (t2).</li>
    <li>Record       your observations in tabular form.</li>
</ul>
<p><strong>Note: </strong>Pieces of pumice stone are added to water before heating to  avoid bumping of liquid when the temperature rises. When water boils it  releases energy as bubbles. If the bubbles do not form, the water can super heat  and possibly explode. This could happen if the container is very clean and  there is no means for the formation of bubbles.  Adding pieces of rough stone gives lots of surface area for bubbles to  form and release the energy gradually.</p>
