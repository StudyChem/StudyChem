<div class="divContent"><p>An <span style="color: rgb(255, 102, 0);"><strong>exothermic reaction</strong></span> is a chemical reaction that releases energy in the form of light or heat.</p>
<p style="text-align: center;"><span style="color: rgb(255, 102, 0);"><strong>reactants → products + energy</strong></span></p>
<p style="text-align: left;"><em><strong>Examples of <span style="color: rgb(255, 102, 0);">exothermic reactions</span>:</strong></em></p>
<ul>
    <li>&nbsp;&nbsp;&nbsp; Combustion reactions of fuels</li>
    <li>&nbsp;&nbsp;&nbsp; Neutralization reactions such as direct reaction of acid and base</li>
    <li>&nbsp;&nbsp;&nbsp; Adding concentrated acid to water</li>
    <li>&nbsp;&nbsp;&nbsp; Burning of a substance</li>
    <li>&nbsp;&nbsp;&nbsp; Adding water to anhydrous copper(II) sulfate</li>
    <li>&nbsp;&nbsp;&nbsp; The thermite reaction</li>
    <li>&nbsp;&nbsp;&nbsp; Reactions taking place in a self-heating can based on lime and aluminium</li>
    <li>&nbsp;&nbsp;&nbsp; The setting of cement and concrete</li>
    <li>&nbsp;&nbsp;&nbsp; Many corrosion reactions such as oxidation of metals</li>
    <li>&nbsp;&nbsp;&nbsp; Most polymerisation reactions</li>
    <li>&nbsp;&nbsp;&nbsp; The Haber-Bosch process of ammonia production</li>
    <li>&nbsp;&nbsp;&nbsp; Burning of natural gas</li>
    <li>&nbsp;&nbsp;&nbsp; Respiration</li>
    <li>&nbsp;&nbsp;&nbsp; Decomposition of vegetable matter into compost</li>
</ul>
<p>An <span style="color: rgb(51, 153, 102);"><strong>endothermic reaction</strong></span> is any chemical reaction that absorbs heat from its environment.</p>
<p style="text-align: center;"><span style="color: rgb(51, 153, 102);"><strong>reactants + energy → products</strong></span><strong><br>
</strong></p>
<p><em><strong>Example of <span style="color: rgb(51, 153, 102);">endothermic reactions</span> :</strong></em></p>
<ul>
    <li>&nbsp;&nbsp;&nbsp; Reaction of barium hydroxide octahydrate crystals with dry ammonium chloride</li>
    <li>&nbsp;&nbsp;&nbsp; Dissolving ammonium chloride in water</li>
    <li>&nbsp;&nbsp;&nbsp; Reaction of thionyl chloride (SOCl2) with cobalt(II) sulfate heptahydrate</li>
    <li>&nbsp;&nbsp;&nbsp; Mixing water and ammonium nitrate</li>
    <li>&nbsp;&nbsp;&nbsp; Mixing water with potassium chloride</li>
    <li>&nbsp;&nbsp;&nbsp; Reacting ethanoic acid with sodium carbonate</li>
    <li>&nbsp;&nbsp;&nbsp; Photosynthesis (chlorophyll is used to react carbon dioxide plus water plus energy to make glucose and oxygen)</li>
</ul>
<p><span style="display: none;" id="1340363611012E">&nbsp;</span><span style="color: rgb(51, 153, 102);"><em><strong>Endothermic</strong></em></span><em><strong> processes are as follows :</strong></em></p>
<ul>
    <li>&nbsp;&nbsp;&nbsp; Melting ice cubes</li>
    <li>&nbsp;&nbsp;&nbsp; Melting solid salts</li>
    <li>&nbsp;&nbsp;&nbsp; Evaporating liquid water</li>
    <li>&nbsp;&nbsp;&nbsp; Converting frost to water vapor (melting, boiling, and evaporation in general are endothermic processes)</li>
    <li>&nbsp;&nbsp;&nbsp; Making an anhydrous salt from a hydrate</li>
    <li>&nbsp;&nbsp;&nbsp; Forming a cation from an atom in the gas phase</li>
    <li>&nbsp;&nbsp;&nbsp; Splitting a gas molecule</li>
    <li>&nbsp;&nbsp;&nbsp; Separating ion pairs</li>
</ul>
