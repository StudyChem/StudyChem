<h1>The Theory</h1>
<h2>Can you define pH?</h2>
<p>pH is  defined as the negative logarithm(base 10) of the hydrogen ion concentration in  moles per litre. pH is written as:</p>
<p style="text-align: center;"><img alt="" src="<?php echo base_url();?>assests/img/phEqn1.png" height="25" align="middle" width="148"></p>
<p>Hydrogen ions cannot exist alone, but they exist after combining with water molecules. Thus, H<sup>+</sup>must always be shown as H<sup>+</sup>(aq) or hydronium ion(H<sub>3</sub>O<sup>+sup>):</p>
<p style="text-align: center;"><sup><img alt="" src="<?php echo base_url();?>assests/img/phEqn2.png" height="25" align="top" width="157"></sup></p>
<h2>How do we represent the pH of a neutral solution?&nbsp;</h2>
<p style="text-align: center;"><img alt="" src="<?php echo base_url();?>assests/img/phEqn3.png" height="25" align="middle" width="148"></p>
<p style="text-align: center;"><img alt="" src="<?php echo base_url();?>assests/img/phlasteqn.gif" height="29" width="185"></p>
<p>Neutral solution has pH value 7. pH of acid solution is always less than 7,&nbsp; whereas that of alkaline solution is always more than 7. pH of pure water is 7 or [H+]=10<sup>-7</sup> mol/L .</p>
<h2>What is a pH indicator?</h2>
<p>pH indicator is a chemical that turns different colours in different media. For example, blue litmus turns red in acidic medium, the red litmus turns blue in alkaline medium.</p>
<h2>What is a litmus solution?</h2>
<p>Litmus solution is a purple dye, which is extracted from a Thallophyte known as Lichen. Litmus solution when in neutral medium (i.e., neither acidic nor basic) is greenish in colour.</p>
<h2>What are the commonly used indicators to determine pH of a solution?</h2>
<p>Litmus solution and litmus paper are commonly used as an indicator. pH paper is a piece or a strip of paper which is coated with pH-indicator.</p>
<h2>Know what a pH scale is?</h2>
<p>A scale for measuring hydrogen ion concentration is known as pH scale. In the term pH, p stands for &#039;<em>potenz</em>&#039; in German meaning power.</p>
