<h1>Our Objective</h1>
<p>To determine the melting point of ice.</p>
<h1>The Theory</h1>
<h2>Can you define the melting point of a solid?</h2>
<p>Melting  point of a solid is defined as the temperature at which a solid changes into a  liquid. Melting point of a solid indicates the strength of the force of  attraction between the particles of the solid. On heating a solid, its molecules  absorb energy in the form of heat and their kinetic energy increases.</p>
<p>As the  kinetic energy increases, the temperature of the solid increases. As a result,  the force of attraction between the molecules decreases and the molecules  become more and more separated.&nbsp; This increases the potential energy of the  molecule and the particles leave their fixed positions and start moving more  freely.</p>
<p>At a particular temperature, the separation of the molecules increases  by a large amount and the solid melts and converts into liquid. This particular  temperature is the melting point of that solid. The melting point of a pure  substance is always higher than the melting point of that substance when a  small amount of an impurity is present. Pressure also affects the melting point  of a substance. As the pressure on the substance increases, the melting point  decreases.</p>
<h2>What are the benefits of knowing the melting point  of a compound?</h2>
<p>The melting  point of a compound helps in the identification of that compound. Measurement of  the melting point of a solid can also provide information on the purity of the  substance. Melting point of mixtures is lower than the melting point of a pure  solid.&nbsp; Pure crystalline solids melt over a very narrow range of  temperatures, whereas mixtures melt over a broad range of temperatures.</p>
<p>The  temperature of the reverse change from liquid to solid is called the freezing  point or crystallization point. For most substances the melting point and  freezing points are equal.</p>
<p>For ice,  the melting point is 0째C or 273K. The chemical element with the highest melting  point is tungsten (3410째C), which is used for making filaments in light bulbs.</p>
<p>During the  melting process, till all the ice melts, the temperature of the system does not  increase until after the melting point is reached. The whole of the supplied  heat energy is consumed in increasing the potential energy of the ice  molecules. The kinetic energy of the molecule does not increase further, and the  temperature does not increase, so long as the melting continues.</p>
<p>The  quantity of heat required to completely change 1 kg of ice into water at  atmospheric pressure at its melting point is called latent heat of fusion. For  ice, it is 3.34 x 10<sup>5</sup> J/kg at 0째C.&nbsp;</p>
