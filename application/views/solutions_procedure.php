<h2>Simulator Procedure:</h2>
<ol>
    <li>You are provided with:
    <ol type="a">
        <li>True solutions of sugar in test tube A, salt in  test tube B and alum in test tube C.</li>
        <li>Suspensions of soil in test tube D, chalk in test  tube E, sand in test tube F.</li>
        <li>Colloids of starch in test tube G and egg albumin  in test tube H.</li>
    </ol>
    </li>
    <li>To distinguish between the solutions, under the  ‘Select  Properties’; select any one of the properties (Transparency, Filtration   or Stability).</li>
</ol>
<h3>To check the Transparency:</h3>
<ol start="3">
    <li>Click on any one of the solution sets (True  Solutions, Suspensions or Colloids) to find its properties.</li>
    <li>Click and drag the cellophane paper behind the  solutions in the test tubes to check the transparency of the solutions.</li>
    <li>An information icon pops up. Click on the  information icon to see the inference.</li>
</ol>
<h3>To do the Filtration:</h3>
<ol start="6">
    <li>Click on any one of the solution sets (True  Solutions, Suspensions or Colloids) to find its properties.</li>
    <li>Click on any one of the test tubes to pour the  solution into the funnel.</li>
    <li>An information icon pops up. Click on the  information icon to see the inference.</li>
</ol>
<h3>To check the Stability:</h3>
<ol start="9">
    <li>Click on any one of the solution sets (True  Solutions, Suspensions or Colloids) to find its properties.</li>
    <li>Wait till  the particles settle down.</li>
    <li>An  information icon pops up. Click on the information icon to see the inference.</li>
</ol>
<h2>Inferences:</h2>
<ol start="1" type="1">
    <li>Colloidal       solutions are somewhat translucent and their particles can pass through       filter paper to give translucent filtrate. No residue particles are left       as residue on the filter paper.</li>
    <li>True       solutions are transparent. They pass through filter paper leaving no       residue on the filter paper. The filtrate is also transparent.</li>
    <li>Suspensions       are somewhat&nbsp; opaque or dull. They leave residue particles on filter paper on filtration. The filtrate is more or less clear and       transparent.</li>
</ol>
<h2>Precautions:</h2>
<ol>
    <li>While filtering a solution drop the contents along a glass rod into the funnel.</li>
    <li>Keep the sample undisturbed while checking stability.</li>
    <li>Handle acids carefully.&nbsp;</li>
</ol>
