<h2>Real Lab Procedure:</h2>
<h3>Preparation of a true solution of common salt, sugar and alum</h3>
<p>Take three beakers with 90 ml water in each. Label them as A, B, and C. Add 10 g of finely powdered common salt, sugar and alum (<strong>phitkari</strong>) in beakers A, B and C respectively. Stir the solutions in each beaker thoroughly with the help of a glass rod and when ready keep them for further use.<br>
&nbsp;</p>
<ul>
    <li><strong>Preparation of a suspension of soil, chalk powder and fine sand in water</strong></li>
</ul>
<p>Take three beakers. Put 90 ml of water in each beaker. Label them as D, E, and F. Add 10 g of&nbsp; fine soil (without litter), fine chalk powder and fine sand in beaker A, B and C respectively. Stir the contents of each beaker well with the help of a glass rod.</p>
<ul>
    <li><strong>A colloidal of starch in water</strong></li>
</ul>
<p>Mix 1% dry corn starch with 3 ml of distilled water. To this add 97 ml of boiling distilled water. Stir it well. Heat it for 2 minutes stirring the solution constantly. Cool it and store it for further use. Label the test tube as G.</p>
<ul>
    <li><strong>A colloidal of egg albumin in water</strong></li>
</ul>
<p>Take 1 g of egg albumin (the colourless fluid found around the yellow yolk) and 5 ml of distilled water in a beaker. Mix it well. Slowly add 95 ml of distilled water while stirring continuously. After mixing, add a few drops of acid (dil.HCl/dil.H<sub>2</sub>SO<sub>4 </sub>or acetic acid) and stir well. The clear solution of albumin and water will become turbid. Label the test tube as H.</p>
