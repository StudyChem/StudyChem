    </ul>
</ul>
<h2>Why are decomposition reactions mostly endothermic in nature?</h2>
<p>Most decomposition reactions require energy either in the form of heat, light or electricity. Absorption of energy causes the breaking of the bonds present in the reacting substance which decomposes to give the product.</p>
