<h1>The Theory</h1>
<h2>What is a decomposition reaction?</h2>
<p>Decomposition is  a type of chemical reaction and it may be defined as the reaction in which a  single compound splits into two or more simple substances under suitable  conditions. It is just the opposite of the combination reaction.</p>
<p>In a combination reaction, a substance is formed as a result of chemical combination, while in a decomposition reaction, the substance breaks into new substances.</p>
<p><img alt="" src="<?php echo base_url();?>assests/img/Decomposition%20reaction%20theory.png" height="425" width="900"></p>
<p>For example:&nbsp; The digestion of food in our body is accompanied by a number of decomposition reactions. The major constituents of our food such as carbohydrates, fats proteins, etc., decompose to form a number of simpler substances.&nbsp; These substances further react, releasing large amounts of energy, which keeps our body working.</p>
