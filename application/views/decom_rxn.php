<h2>Types of Decomposition Reactions</h2>
<p style="text-align: left;">Decomposition  reactions can be classified into three types.</p>
<ul type="1">
    <li>Thermal decomposition reaction</li>
    <li>Electrolytic decomposition  reaction</li>
    <li>Photo decomposition reaction</li>
</ul>
<ul type="1">
    <li><strong>Thermal decomposition  reaction (Thermolysis)</strong></li>
    <p>Thermal decomposition is a chemical  reaction where a single substance breaks into two or more simple substances  when heated. The reaction is usually endothermic because heat is required to  break the bonds present in the substance.<br>
    Examples:</p>
    <ul type="disc">
        <li><strong>Decomposition of calcium carbonate:</strong> Calcium  carbonate (lime stone) decomposes into calcium oxide (quick lime) and carbon  dioxide when heated. Quick lime is the major constituent of cement.
        <p style="text-align: center;"><img src="<?php echo base_url();?>assests/img/DecompositionReactionEqn1.gif" alt="" align="middle" height="27" width="292"><br>
        &nbsp;</p>
        </li>
        <li><strong>Decomposition of potassium chlorate:</strong> When heated strongly, potassium chlorate decomposes into potassium chloride and  oxygen. This reaction is used for the preparation of oxygen.
        <p style="text-align: center;"><img alt="e2" src="<?php echo base_url();?>assests/img/DecompositionReactionEqn2.gif" align="middle" height="30" width="288"></p>
        <p>If the decomposition is carried out in the presence of manganese  dioxide (MnO<sub>2</sub>), it takes place at a lower temperature. In this case,  MnO<sub>2</sub> is used as a catalyst.</p>
        </li>
        <li><strong>Decomposition of ferric  hydroxide:</strong> Ferric hydroxide undergoes decomposition  in the presence of heat, giving ferric oxide and water molecules.
        <p style="text-align: center;"><img src="<?php echo base_url();?>assests/img/DecompositionReactionEqn3.gif" alt="" align="middle" height="28" width="276"></p>
        <p>&nbsp;</p>
        </li>
        <li><strong>Decomposition of hydrated  oxalic acid:</strong>
        <p>Hydrated oxalic acid (H<sub>2</sub>C<sub>2</sub>O<sub>4</sub>.2H<sub>2</sub>O)  decomposes into oxalic acid and water when heated.</p>
        <p style="text-align: center;"><img src="<?php echo base_url();?>assests/img/DecompositionReactionEqn4.gif" alt="" align="middle" height="29" width="315"></p>
        </li>
    </ul>
    <li><strong>Electrolytic decomposition  reaction (Electrolysis)</strong>
    <p>Electrolytic  decomposition may result when electric current is passed through an aqueous  solution of a compound. A good example is the electrolysis of water.</p>
    <ul type="disc">
        <li><strong>Electrolysis of water:</strong> Electrolysis of water is the decomposition of water into hydrogen  and oxygen due to the passage of electric current through it.</li>
        <p style="text-align: center;"><img src="<?php echo base_url();?>assests/img/DecompositionReactionEqn5.gif" alt="" align="middle" height="22" width="281"><img alt="" src="<?php echo base_url();?>assests/img/cls10_decomp_react11.jpg" align="right" height="203" width="151"></p>
        <p>&nbsp;</p>
        <li><strong>Decomposition of sodium  chloride:</strong> On passing electricity through molten  sodium chloride, it decomposes into sodium and chlorine.</li>
        <p style="text-align: center;"><br>
        <img src="<?php echo base_url();?>assests/img/DecompositionReactionEqn6.gif" alt="" align="middle" height="29" width="212"></p>
    </ul>
    </li>
    <li><strong>Photo decomposition  reaction (Photolysis)</strong></li>
    <p>Photo decomposition  is a chemical reaction in which a substance is broken down into simple  substances by exposure to light (photons).</p>
    <ul type="disc">
        <li><strong>Decomposition of silver  chloride:</strong> Place a small quantity of silver chloride  (AgCl) taken in a watch glass under sunlight for some time. The crystals  slowly acquire a grey colour. On analysis, it is found that the sunlight has  caused decomposition of silver chloride into silver and chlorine.
