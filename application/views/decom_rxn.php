        </li>
        <p style="text-align: center;">&nbsp;</p>
        &nbsp; &nbsp;&nbsp;
        <li><strong>Decomposition of hydrogen  peroxide:</strong> In the presence of light, hydrogen  peroxide decomposes into water and oxygen
