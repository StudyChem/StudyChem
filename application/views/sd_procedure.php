<div class="divContent"><h1>Materials required:</h1>
<ul><img alt="" src="<?php echo base_url();?>assests/img/Single%20displacement%20reaction%20materials.png" height="425" width="900"></ul>
    <h1>Procedure:</h1>
    <ul>
        <li>Clean two iron nails of sufficient size by rubbing them with sand  paper so that their colour appears grayish.</li>
        <li>Take sufficient quantity of copper sulphate solution in two  different test tubes.</li>
        <li>Clamp the test tubes to different stands.</li>
        <li>Tie one nail using a thread and hang in one test tube. Tie the other  end of the thread to the stand.</li>
        <li>Keep the other nail in a Petri dish for comparison after the  experiment.</li>
        <li>Keep the two test tubes undisturbed for about 15 minutes.</li>
        <li>Remove the iron nail immersed in the copper sulphate solution and  put it in the Petridish.</li>
    </ul>
    <h1>Simulator Procedure:</h1>
    <ul>
        <li>First, click on the iron nail tied with a thread to immerse it  in one of the test tubes  containing copper sulphate solution.</li>
        <li>This done, wait for 15 minutes.</li>
        <li>You can see the inference by clicking on the  inference icon.</li>
        <li>&nbsp;Click on the thread to remove iron nail from the test tube.</li>
        <li>&nbsp;You can see the zoom view of part of the test  tubes or iron nails by clicking on them.</li>
        <li>You can click the ‘Reset’ button to redo the  experiment.</li>
    </ul>
    <h1>Observations:</h1>
    <ul>
        <li>There is a brown coating on the iron nail dipped in the copper sulphate  solution. Whereas the iron nail placed in the Petri dish shows the grayish colour  of iron.</li>
        <li>The colour of the solution of copper sulphate in which the iron nail  was dipped changes to light greenish, whereas the colour of copper sulphate  solution in the other test tube does not change.</li>
    </ul>
    <h1>Inference:</h1>
    <ul>
        <li>The brown coating on the iron nail shows that copper is deposited on  the iron nail by displacing iron.</li>
        <li>The greenish colour of the solution in the test tube shows that Fe<sup>2+</sup>  ions are present in the solution.</li>
        <li>This shows that iron is more reactive than copper, as Fe<sup>2+</sup>  ions have displaced Cu<sup>2+</sup> ions from copper sulphate solution.</li>
        <li>This is a single displacement reaction in which copper has been  displaced from iron from copper sulphate solution.</li>
    </ul>
